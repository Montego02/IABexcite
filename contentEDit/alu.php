<h2>Leichtgewichte in Serienproduktion</h2>
Aluminium macht Fahrzeuge erheblich leichter – das reduziert den Kraftstoffverbrauch und den CO2-Ausstoß. Aber Aluminium ist auch teuer! Deswegen haben wir gemeinsam mit Novelis, dem globalen Marktführer für Aluminium-Walzprodukte, Tailored Blanks aus Aluminium entwickelt und auf die Straße gebracht. Für Tailored Aluminum Blanks - TAB® verbinden wir Aluminiumbleche unterschiedlicher Dicken und Güten erstmals in Serie miteinander. Das spart Gewicht und Geld, denn der gezielte Einsatz von TAB® senkt die Kosten für den hochpreisigen Werkstoff Aluminium im Fahrzeug. Viele OEMs haben das Herstellungsverfahren bereits freigegeben.



