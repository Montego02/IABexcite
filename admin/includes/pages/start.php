    <h2>Handbuch</h2>
    
    
    <h3>Inhalte</h3>
    Die Text-Inhalte auf der Seite werden im Bereich "Content" verwaltet:
    <ul>
       <li>Klicken Sie im Menu auf "Content"</li>
       <li>Öffnen Sie den zu editierenden Artikel</li>
        <li>Klicken Sie nach der Änderung auf das Speichersymbol <a title="Speichern" class="icon-floppy-disk btn"></a></li>
    </ul>
    
   
     <hr>
     
     <h3>Dateien verwalten</h3>
     Neue Bilder und Dateien, können unter <span class="icon-images"></span> "Medien" verwaltet werden
     <ol>
         <li>Auf Medien klicken</li>
         <li>Es werden horizontal die verfügbaren Ordner angezeigt, ggf. zum gewünschten Ordner navigieren</li>
         <li>Im gewünschten Ordner können Sie auf der rechten Seite unter "Datei hochladen" eine Datei auswählen und hochladen</li>
         <li>Zum Löschen alter Dateien diese anklicken und danach auf das Papierkorbsysmbol rechts oben klicken</li>
     </ol>
   
     
    
